import { mkdir, readFile, writeFile } from 'node:fs/promises'
import process from 'node:process'
import { URL } from 'node:url'

const sourcePath = new URL('../src/domain/themes.ts', import.meta.url)
const outputPath = new URL('../src/assets/generated/fluentIconData.ts', import.meta.url)
const collectionPath = new URL(
  '../node_modules/@iconify-json/fluent-emoji-flat/icons.json',
  import.meta.url,
)
const charactersPath = new URL(
  '../node_modules/@iconify-json/fluent-emoji-flat/chars.json',
  import.meta.url,
)

const [source, collectionText, charactersText] = await Promise.all([
  readFile(sourcePath, 'utf8'),
  readFile(collectionPath, 'utf8'),
  readFile(charactersPath, 'utf8'),
])
const collection = JSON.parse(collectionText)
const characters = JSON.parse(charactersText)
const segmenter = new Intl.Segmenter('ca', { granularity: 'grapheme' })
const emojis = [
  ...new Set(
    [...segmenter.segment(source)]
      .map(({ segment }) => segment)
      .filter((segment) => /\p{Extended_Pictographic}/u.test(segment)),
  ),
].sort((first, second) => first.localeCompare(second))

const unicodeKey = (emoji) =>
  [...emoji]
    .map((character) => character.codePointAt(0).toString(16))
    .filter((codePoint) => codePoint !== 'fe0f')
    .join('-')

const missing = []
const entries = emojis.flatMap((emoji) => {
  const iconName = characters[unicodeKey(emoji)]
  const icon = iconName ? collection.icons[iconName] : undefined
  if (!iconName || !icon) {
    missing.push(`${emoji} (${unicodeKey(emoji)})`)
    return []
  }
  return [
    `  ${JSON.stringify(emoji)}: ${JSON.stringify({
      ...icon,
      width: icon.width ?? collection.width,
      height: icon.height ?? collection.height,
    })},`,
  ]
})

if (missing.length > 0) {
  throw new Error(`Fluent Emoji icons not found:\n${missing.join('\n')}`)
}

const generated = `// Generated by scripts/generate-fluent-icons.mjs. Do not edit manually.
// Artwork: Microsoft Fluent Emoji (MIT), bundled locally through Iconify.
interface FluentIconData {
  readonly body: string
  readonly width?: number
  readonly height?: number
}

export const fluentIconData: Readonly<Record<string, FluentIconData>> = {
${entries.join('\n')}
}
`

if (process.argv.includes('--check')) {
  const current = await readFile(outputPath, 'utf8').catch(() => '')
  if (current !== generated) {
    throw new Error('Fluent icon data is stale. Run pnpm icons:build.')
  }
} else {
  await mkdir(new URL('../src/assets/generated/', import.meta.url), { recursive: true })
  await writeFile(outputPath, generated, 'utf8')
}
